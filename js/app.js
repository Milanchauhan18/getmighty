$(document).ready(function(){
    $('.slider').slick({
        infinite: true,
        slidesToShow: 3,
        slidesToScroll: 3,
        autoplay: true,
        autoplaySpeed: 2000,
        prevArrow: $('.prev'),
          nextArrow: $('.next'),
        responsive: [
          {
            breakpoint: 426,
            settings: {
              slidesToShow: 1,
              slidesToScroll:1,
            }
          },
        ]
      });
});

